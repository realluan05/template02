<?php
function item($text, $href, $isFirst = false)
{
    if ($text && $href) {
        return '<li class="item '. isFirst($isFirst) .'">
                    <a href="#'.$href.'">'.$text.'</a>
                </li>';
    }
    return false;
}

function isFirst($yesno)
{
    if ($yesno) {
        return 'active';
    }
    return false;
}

function dropItem($text, $href)
{
    if ($text && $href) {
        return '<li class="sub-item">
                    <a href="'.$href.'">'.$text.'</a>
                </li>';
    }
    return false;
}
?>

<nav id="navbar">
    <div class="menu-container">
        <ul class="menu">
            <?= item("Home", "download", true); ?>
            <?= item("About", "about-us"); ?>
            <?= item("Pricing", "price"); ?>
            <?= item("Team", "team"); ?>
            <?= item("Contact", "contact"); ?>
            <li class="item has-children">
                <a href="#">Pages</a>
                <div class="dropdown">
                    <ul>
                        <?= dropItem("About Page", "about.php") ?>
                        <?= dropItem("Pricing Page", "pricing.php") ?>
                        <?= dropItem("Contact Page", "contact.php") ?>
                        <?= dropItem("Blog Grid Page", "blog.php") ?>
                        <?= dropItem("Blog Details Page", "blog-details.php") ?>
                        <?= dropItem("Sign in Page", "login.php") ?>
                        <?= dropItem("404 Page", "404.php") ?>
                    </ul>
                </div>
            </li>
        </ul>
    </div>
</nav>